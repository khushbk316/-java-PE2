package com.stackroute.pe2;

public class MemberVariablesAndMethods {
    private String Name;
    private int age;
    private int salary;

    public void setName(String name) {
        Name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }


    public String getName() {
        return Name;
    }

    public int getAge() {
        return age;
    }

    public int getSalary() {
        return salary;
    }

}








